    private void populaTabelaProduto() throws SQLException, ClassNotFoundException{
        tbtCliente.removeAll();
        List<Cliente> clientes = new ArrayList<Cliente>();
        Cliente f = null;
        // Pega os dados do Banco de Dados
        // public Cliente(String nome, String email, String cep, String endereco, String cidade, String senha,String cpf,int telefone) {
        try{
            Connection con = c.criaConexao();
            ResultSet dados =  CRUDTodos.PesquisarReclamacao(con);
            while (dados.next()){
                 f = new Cliente(dados.getString("cliNome"),dados.getString("cliEmail"),dados.getString("cliCEP"),dados.getString("cliAddress"),dados.getString("cliCidade"),
                         dados.getString("cliPsw"),dados.getString("cliCPF"),dados.getInt("cliTel"));
                 clientes.add(f);
             }
             con.close();
         } 
        catch(SQLException e)
        {}
       // Cria o modelo e associa com o List
       eModelCliente = new clienteModeloDeTabela(clientes);
       // Associa o modelo à tabela
       tbtCliente.setModel(eModelCliente);
    }
