    private void btnAReclamcao_EnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAReclamcao_EnviarActionPerformed
        // TODO add your handling code here:
        // public FichaReclamacao(int ClienteCodigo, String Tipo, String Descricao, Date dataEmicao ,int idArquivo) {
        String item;
        Connection con;
        try {
            con = c.criaConexao();
            if(cbx_areaReclamacao.getSelectedItem()!=null)
                item = cbx_areaReclamacao.getSelectedItem().toString();
            else if(cbx_areaReclamacao.getSelectedItem() == "Outro");
                item = txtAreaReclamacao.getText();
            
        java.util.Date data = new java.util.Date();
        java.sql.Date dataSql = new java.sql.Date(data.toInstant().getEpochSecond());
         
        CRUDTodos.InserirReclamacao(new FichaReclamacao(cpfSelected,item,txtMensagem.getText(),dataSql,uniqueID), con);
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(TelaAbrirReclamacao.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAReclamcao_EnviarActionPerformed

